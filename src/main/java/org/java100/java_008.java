package org.java100;

import java.util.Scanner;

class java_008 {
    public static void main(String[] args) {
        System.out.println(new Scanner(System.in).nextLine());
    }
}

/*
    키보드로 입력한 문자를 그대로 출력하는 프로그램을 작성

        입력
        문자 한 개가 입력된다.
        출력
        입력된 문자를 그대로 출력한다.

        입력 예시
        p
        출력 예시
        p


        Scanner 클래스를 사용한다. nextLine() 메소드 사용.
*/