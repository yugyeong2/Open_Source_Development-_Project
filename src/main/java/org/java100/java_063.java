package org.java100;

import java.util.Scanner;

class java_063 {
    public static void main(String[] args) {
        int day = new Scanner(System.in).nextInt();
        System.out.println(day * 24);
    }
}

/*
    하루는 24시간이다.
    일(day)이 입력으로 주어지면 시간으로 변환하시오.

        입력
        일(day)이 입력된다.
        출력
        시간으로 변환해서 출력한다.

        입력 예시
        2
        출력 예시
        48
*/