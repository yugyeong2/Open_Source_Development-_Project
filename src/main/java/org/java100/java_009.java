package org.java100;

import java.util.Scanner;

class java_009 {
    public static void main(String[] args) {
        System.out.println(new Scanner(System.in).nextFloat());
    }
}

/*
    키보드로 입력한 실수(소수점이 있는 수)를 그대로 출력하는 프로그램을 작성

        입력
        실수 한 개가 입력된다.
        출력
        입력된 실수를 그대로 출력한다.

        입력 예시
        1.414
        출력 예시
        1.414

        Scanner 클래스를 사용한다. nextFloat() 메소드 사용.
*/